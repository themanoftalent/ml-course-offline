  <p class="body-text">
    In many situations, we learn by interacting with our environment. We observe
    the current situation, take actions, and observe outcomes, and then reflect
    on these previous occurrences when we take our next action. The field of
    psychology has historically studied the human ability to learn from the
    reinforcement of behaviors, and this influenced the development of an area
    of machine learning: <span class="bold">reinforcement learning</span>. Reinforcement learning (RL)
    is an area of machine learning that focuses on how learning from interacting
    with an environment can occur effectively for intelligent agents.
    <br /><br />
    The goal of RL problems is to learn which actions to take and when to take them
    to maximize cumulative reward over time. This differs from supervised learning,
    as the decision maker is not told what is the correct action in the current situation
    and instead must learn from the consequences of its own actions. It is also different
    from unsupervised learning, as the goal is not to find structure in unlabeled
    data but instead to maximize reward.
